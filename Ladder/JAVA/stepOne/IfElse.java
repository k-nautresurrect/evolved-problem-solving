import java.lang.*;

public class IfElse {
	public static void main(String[] args) {
		boolean condition = true;

		// complete code block
		if (condition == true) {
			// do work
		} else {
			// execute if "if" not executed
		}
		// else if ladder
		if (condition == true) {
			// do something
		} else if (condition == true) {
			// do this thing if "if" not executed
		} else if (condition == true) {
			// do this thing if "else if" not executed
		}

		// seprated works
		if (condition == true) {
			// this work
		}
		if (condition == true) {
			// this work independent of previous if
		}

	}
}